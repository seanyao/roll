interface Run {
  status: number;
  stdout: string;
  stderr: string;
}

function envBase(home: string, extra: Record<string, string>): Record<string, string> {
  return {
    PATH: `${fakeBin}:${process.env["PATH"] ?? ""}`,
    HOME: home,
    ROLL_HOME: join(home, ".roll"),
    NO_COLOR: "1",
    ROLL_LANG: "en",
    ...extra,
  };
}

function readLcLog(): string {
  const s = existsSync(lcLog) ? readFileSync(lcLog, "utf8") : "";
  rmSync(lcLog, { force: true });
  return s;
}

const ENV_KEYS = [
  "PATH", "HOME", "ROLL_HOME", "NO_COLOR", "ROLL_LANG", "LC_ALL", "LANG",
  "_LAUNCHD_DIR", "ROLL_LOOP_AGENT", "ROLL_CYCLE_LOG_RAW",
];

function tsOb(fx: Fixture, args: string[], extra: Record<string, string>): Run {
  const target = envBase(fx.home, extra);
  const save: Record<string, string | undefined> = {};
  for (const k of ENV_KEYS) save[k] = process.env[k];
  for (const k of ENV_KEYS) delete process.env[k];
  for (const [k, v] of Object.entries(target)) process.env[k] = v;
  const saveCwd = process.cwd();
  process.chdir(fx.proj);
  const outChunks: string[] = [];
  const errChunks: string[] = [];
  const realOut = process.stdout.write.bind(process.stdout);
  const realErr = process.stderr.write.bind(process.stderr);
  // @ts-expect-error capture-only
  process.stdout.write = (c: string | Uint8Array): boolean => (outChunks.push(String(c)), true);
  // @ts-expect-error capture-only
  process.stderr.write = (c: string | Uint8Array): boolean => (errChunks.push(String(c)), true);
  let status: number;
  try {
    status = offboardCommand(args);
  } finally {
    process.stdout.write = realOut;
    process.stderr.write = realErr;
    process.chdir(saveCwd);
    for (const k of ENV_KEYS) {
      const v = save[k];
      if (v === undefined) delete process.env[k];
      else process.env[k] = v;
    }
  }
  return { status, stdout: outChunks.join(""), stderr: errChunks.join("") };
}

/** Strip a fixture's (per-run, random) project path so the two runs compare. */
function norm(run: Run, proj: string): Run {
  // bash resolves cwd via `pwd -P`; realpath the proj so the substitution hits
  // the same bytes the command printed.
  let real = proj;
  try {
    real = realpathSync(proj);
  } catch {
    /* keep */
  }
  const repl = (s: string): string => s.split(real).join("<PROJ>").split(proj).join("<PROJ>");
  return { status: run.status, stdout: repl(run.stdout), stderr: repl(run.stderr) };
}

// US-PORT-021b: bash oracle retired → freeze the proven-correct, proj-normalized
// TS output as a snapshot (CI is the cross-platform gate).
function both(build: () => Fixture, args: string[], extra: Record<string, string> = {}): void {
  const tf = build();
  expect(norm(tsOb(tf, args, extra), tf.proj)).toMatchSnapshot();
}

describe("diff-test: roll offboard == bash oracle", () => {
  it("--help → usage, exit 0", () => {
    both(docOnly, ["--help"]);
  });

  for (const lang of ["en", "zh"]) {
    it(`no changeset → exit 1 (${lang})`, () => {
      both(noChangeset, [], { ROLL_LANG: lang });
    });

    it(`empty changeset → nothing to offboard (${lang})`, () => {
      both(emptyChangeset, [], { ROLL_LANG: lang });
    });

    it(`unknown flag → exit 1 (${lang})`, () => {
      both(docOnly, ["--bogus"], { ROLL_LANG: lang });
    });

    it(`doc-only dry-run → plan preview (${lang})`, () => {
      both(docOnly, [], { ROLL_LANG: lang });
    });

    it(`doc-only --confirm → apply (${lang})`, () => {
      both(docOnly, ["--confirm"], { ROLL_LANG: lang });
    });

    it(`with-plists dry-run → plan lists plist (${lang})`, () => {
      both(withPlists, [], { ROLL_LANG: lang });
    });

    it(`cross-project path → refuse, exit 1 (${lang})`, () => {
      both(crossProject, ["--confirm"], { ROLL_LANG: lang });
    });
  }

  it("with-plists --confirm → launchctl argv identical", () => {
    const tf = withPlists();
    const t = norm(tsOb(tf, ["--confirm"], {}), tf.proj);
    const tLog = readLcLog().split(realpathSync(tf.home)).join("<HOME>").split(tf.home).join("<HOME>");
    expect(t).toMatchSnapshot();
    // launchctl argv frozen (HOME prefix normalized — each run has its own).
    expect(tLog).toMatchSnapshot();
    expect(tLog).toContain("unload\n-w\n");
    expect(tLog).toContain("com.roll.test.loop.plist\n");
  });

  it("with-plists --confirm inside loop cycle → FIX-125 refuse", () => {
    both(withPlists, ["--confirm"], { ROLL_LOOP_AGENT: "pi" });
  });

  it("TS offboard strips Roll-owned AGENTS marker blocks without deleting owner content", () => {
    const fx = mergedAgents();
    const run = tsOb(fx, ["--confirm"], {});

    expect(run.status).toBe(0);
    const agents = readFileSync(join(fx.proj, "AGENTS.md"), "utf8");
    expect(agents).toContain("# Owner Guide");
    expect(agents).toContain("Keep this owner text.");
    expect(agents).not.toContain("<!-- roll:onboard:start -->");
    expect(agents).not.toContain("# Agent Conventions");
    expect(existsSync(join(fx.proj, ".roll", "onboard-changeset.yaml"))).toBe(false);
  });

  it("TS offboard strips Roll-owned CLAUDE marker blocks without deleting owner content", () => {
    const fx = mergedClaude();
    const run = tsOb(fx, ["--confirm"], {});

    expect(run.status).toBe(0);
    const claude = readFileSync(join(fx.proj, ".claude", "CLAUDE.md"), "utf8");
    expect(claude).toContain("# Owner Claude Guide");
    expect(claude).toContain("Keep this Claude owner text.");
    expect(claude).not.toContain("<!-- roll:onboard:start -->");
    expect(claude).not.toContain("# Claude Conventions");
    expect(existsSync(join(fx.proj, ".roll", "onboard-changeset.yaml"))).toBe(false);
  });

  it("TS offboard refuses path traversal in merged-file metadata", () => {
    const fx = traversalMergedFile();
    const run = tsOb(fx, ["--confirm"], {});

    expect(run.status).toBe(1);
    expect(run.stderr).toContain("Refusing to act on '../outside/AGENTS.md'");
    expect(existsSync(join(fx.proj, ".roll", "onboard-changeset.yaml"))).toBe(true);
  });
});
