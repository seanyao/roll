/**
 * diff-test: TS `roll offboard` == bash `bin/roll offboard` (frozen v2 oracle).
 *
 * cmd_offboard's --confirm path mutates the filesystem (removes files/dirs,
 * strips .gitignore lines, unloads + removes launchd plists, deletes the
 * changeset). Each scenario is therefore built TWICE from an identical builder
 * — one fixture for the bash run, one for the TS run — and we byte-compare
 * stdout/stderr/exit. For the plist case the launchctl call is dispatched to a
 * PATH-installed fake `launchctl` that records its argv; we assert the two
 * recordings are identical.
 *
 * CI portability: every run uses a fabricated HOME (so $HOME/Library/LaunchAgents
 * is sandboxed) and a fabricated ROLL_HOME with a seeded update-check cache.
 * `_LAUNCHD_DIR` is left at its canonical default ($HOME/Library/LaunchAgents)
 * so `_launchctl_safe`'s real-binary path reaches the shim; the shim never
 * touches host launchd. No network, no gh, no git. Locale is pinned to keep the
 * bash oracle off the macOS AppleLanguages fallback (en/zh cases override).
 */
import { execSync } from "node:child_process";
import { mkdirSync, mkdtempSync, readFileSync, realpathSync, rmSync, writeFileSync, existsSync } from "node:fs";
import { tmpdir } from "node:os";
import { join, resolve } from "node:path";
import { afterAll, beforeAll, describe, expect, it } from "vitest";
import { offboardCommand } from "../src/commands/offboard.js";
import { seedUpdateCheckCache } from "./helpers.js";

const REPO = resolve(__dirname, "../../..");
const dirs: string[] = [];
let fakeBin = "";
let lcLog = "";

beforeAll(() => {
  fakeBin = mkdtempSync(join(tmpdir(), "roll-ob-bin-"));
  dirs.push(fakeBin);
  lcLog = join(fakeBin, "launchctl-argv.log");
  // Fake launchctl: truncate then append each argv line, exit 0.
  const lc = join(fakeBin, "launchctl");
  writeFileSync(
    lc,
    ["#!/bin/sh", `: > '${lcLog}'`, 'for a in "$@"; do printf "%s\\n" "$a" >> ' + `'${lcLog}'; done`, "exit 0"].join("\n") + "\n",
    { mode: 0o755 },
  );
});

afterAll(() => {
  for (const d of dirs) execSync(`rm -rf '${d}'`);
});

function freshHome(): string {
  const home = mkdtempSync(join(tmpdir(), "roll-ob-home-"));
  dirs.push(home);
  mkdirSync(join(home, ".roll"), { recursive: true });
  seedUpdateCheckCache(join(home, ".roll"));
  return home;
}

function w(base: string, rel: string, content: string): void {
  const full = join(base, rel);
  mkdirSync(join(full, ".."), { recursive: true });
  writeFileSync(full, content);
}

